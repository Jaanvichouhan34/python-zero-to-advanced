# Operators in Python

# Arithmetic Operators
a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Floor Division:", a // b)
print("Exponent:", a ** b)

print()

# Comparison Operators
x = 5
y = 8

print("x == y:", x == y)
print("x != y:", x != y)
print("x > y:", x > y)
print("x < y:", x < y)
print("x >= y:", x >= y)
print("x <= y:", x <= y)

print()

# Logical Operators
p = True
q = False

print("p and q:", p and q)
print("p or q:", p or q)
print("not p:", not p)

print()

# Assignment Operators
num = 10
num += 5
print("After += :", num)

num -= 3
print("After -= :", num)

num *= 2
print("After *= :", num)

print()

# Membership Operators
text = "python"

print("'p' in text:", 'p' in text)
print("'z' not in text:", 'z' not in text)

print()

# Identity Operators
a = 10
b = 10
c = 20

print("a is b:", a is b)
print("a is not c:", a is not c)
